package com.meshtalk.app.ui.theme

import android.app.Activity
import android.os.Build
import androidx.compose.foundation.isSystemInDarkTheme
import androidx.compose.material3.*
import androidx.compose.runtime.Composable
import androidx.compose.runtime.SideEffect
import androidx.compose.ui.graphics.toArgb
import androidx.compose.ui.platform.LocalContext
import androidx.compose.ui.platform.LocalView
import androidx.core.view.WindowCompat

private val LightColorScheme = lightColorScheme(
    primary = MeshPrimary,
    onPrimary = MeshOnPrimary,
    primaryContainer = MeshPrimaryLight,
    onPrimaryContainer = MeshOnPrimary,
    secondary = MeshSecondary,
    onSecondary = MeshOnSecondary,
    secondaryContainer = MeshSecondaryLight,
    onSecondaryContainer = MeshSecondaryDark,
    tertiary = MeshTertiary,
    onTertiary = MeshOnSecondary,
    tertiaryContainer = MeshTertiaryLight,
    onTertiaryContainer = MeshTertiaryDark,
    background = MeshBackground,
    onBackground = MeshOnBackground,
    surface = MeshSurface,
    onSurface = MeshOnSurface,
    surfaceVariant = MeshSurfaceVariant,
    onSurfaceVariant = MeshOnSurface,
    error = SOSRed,
    onError = MeshOnSecondary
)

private val DarkColorScheme = darkColorScheme(
    primary = MeshPrimary,
    onPrimary = MeshOnPrimary,
    primaryContainer = MeshPrimaryDark,
    onPrimaryContainer = MeshPrimaryLight,
    secondary = MeshSecondaryLight,
    onSecondary = MeshSecondaryDark,
    secondaryContainer = MeshSecondaryDark,
    onSecondaryContainer = MeshSecondaryLight,
    tertiary = MeshTertiaryLight,
    onTertiary = MeshTertiaryDark,
    tertiaryContainer = MeshTertiaryDark,
    onTertiaryContainer = MeshTertiaryLight,
    background = MeshBackgroundDark,
    onBackground = MeshOnBackgroundDark,
    surface = MeshSurfaceDark,
    onSurface = MeshOnSurfaceDark,
    surfaceVariant = MeshSurfaceVariantDark,
    onSurfaceVariant = MeshOnSurfaceDark,
    error = SOSRed,
    onError = MeshOnSecondary
)

@Composable
fun MeshTalkTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = MeshTalkTypography,
        content = content
    )
}

